/* eslint-disable @typescript-eslint/no-require-imports */
const fs = require('fs')

let licenseHeader = fs.readFileSync('LICENSE').toString()
licenseHeader =
  '/*\n' +
  ' *' +
  licenseHeader.replaceAll(/\n/g, '\n *') +
  '\n*/'

const generatedLicence =
  '/**\n' +
  ' * RDEPOT API\n' +
  ' * No description provided (generated by Swagger Codegen https://github.com/swagger-api/swagger-codegen)\n' +
  ' *\n' +
  ' * OpenAPI spec version: v2\n' +
  ' * \n' +
  ' *\n' +
  ' * NOTE: This class is auto generated by the swagger code generator program.\n' +
  ' * https://github.com/swagger-api/swagger-codegen.git\n' +
  ' * Do not edit the class manually.\n' +
  ' */'

const needsSerializationGenerated =
  "const needsSerialization = (typeof body !== \"string\") || localVarRequestOptions.headers['Content-Type'] === 'application/json';"
const needsSerialization =
  ' const needsSerialization =\n' +
  "  typeof body !== 'string' ||\n" +
  '  (localVarRequestOptions.headers &&\n' +
  '    localVarRequestOptions.headers[\n' +
  "      'Content-Type'\n" +
  "    ] === 'application/json')"

const basePathGenerated =
  'export const BASE_PATH = "http://localhost:8017/".replace(/\\/+$/, "");'

const basePath =
  'export const BASE_PATH = getEnv(\n' +
  "'VITE_SERVER_ADDRESS'\n" +
  ").replace(/\\/+$/, '')"

const importEnv = "import getEnv from '@/utils/env'\n"

function replaceNeedsSerialization(content) {
  content = content.replaceAll(
    needsSerializationGenerated,
    needsSerialization
  )
  return content
}

function replaceBasePath(content) {
  if (content.indexOf(basePathGenerated) >= 0) {
    content = content.replaceAll(
      basePathGenerated,
      basePath
    )
    content = importEnv + '\n' + content
  }
  return content
}

function replaceLicenseHeader(content) {
  content = content.replace(generatedLicence, '')
  content = licenseHeader + '\n' + content
  return content
}

function updateFiles(dir) {
  for (const file of fs.readdirSync(dir, {
    withFileTypes: true
  })) {
    if (file.isDirectory()) {
      updateFiles(file.path + file.name + '/')
    }
    if (file.isFile() && !file.isDirectory()) {
      const filePath = (file.path ?? '') + file.name
      let content = fs.readFileSync(filePath).toString()
      content = replaceBasePath(content)
      content = replaceNeedsSerialization(content)
      content = replaceLicenseHeader(content)
      saveFile(filePath, content)
    }
  }
}

function saveFile(file, content) {
  fs.writeFile(file, content, 'utf8', function (err) {
    if (err) return console.log(err)
    else console.log('saved to ' + file.toString())
  })
}

updateFiles('src/openapi/')
